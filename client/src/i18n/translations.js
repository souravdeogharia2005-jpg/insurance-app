export function formatCurrency(amount, currency = 'INR') {
    if (!amount && amount !== 0) return currencyConfig[currency].symbol + '0';
    const cfg = currencyConfig[currency];
    const converted = Math.round(amount * cfg.rate);
    return cfg.symbol + converted.toLocaleString(cfg.locale);
}

export function formatDate(date, lang = 'en') {
    if (!date) return '-';
    const d = new Date(date);
    return d.toLocaleDateString(lang === 'hi' ? 'hi-IN' : lang === 'ar' ? 'ar-SA' : lang === 'ja' ? 'ja-JP' : lang + '-' + lang.toUpperCase(), { day: '2-digit', month: 'short', year: 'numeric' });
}

